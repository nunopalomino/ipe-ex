# Criar uma lista com os elementos['a', 'b', 'c', 'd', 'e', 'f', 'g']. Exibir o 3º, 5º e 6º
# elemento, adicionar a string ‘pastel’ como 3º elemento, exibir a lista e
# inverter os elementos da lista (lista reversa) e exibir a lista invertida.

lista = ['a', 'b', 'c', 'd', 'e', 'f', 'g']

print(lista[2])

print(lista[5])

print(lista[6])

lista[2] = 'pastel'

print(lista)

lista.reverse()

